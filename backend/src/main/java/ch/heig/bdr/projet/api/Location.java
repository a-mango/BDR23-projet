package ch.heig.bdr.projet.api;

/**
 * Enum representing the location of a product.
 * IN_STOCK: The product is in stock.
 * FOR_SALE: The product is for sale.
 * RETURNED: The product has been returned.
 * SOLD: The product has been sold.
 */
public enum Location {

    in_stock,
    for_sale,
    returned,
    sold;
}
